package org.javanoprograms;

public class EvenNOto100 {

	public static void main(String[] args) {
			
System.out.println("The Even nos from 1 to 100: "+"");
		
		for (int i = 1; i <=100; i++) {
			
			if (i%2==0) {
				System.out.print(+i+" ");
			} else {

				System.out.println();
				
			}
			
			
		}
	}
}
